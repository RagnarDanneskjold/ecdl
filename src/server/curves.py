def getCurveByName(curveName):
    curve = None
    if( curveName == "secp521r1" ):
        curve = secp521r1
    if( curveName == "secp256k1" ):
        curve = secp256k1 
    if( curveName == "ecp131p" ):
        curve = ecp131

    if curve == None:
        return None 

    return ECCurve(curve.a, curve.b, curve.p, curve.n, curve.x, curve.y)
